uint64_t FLIN(uint64_t y, uint64_t kl) {
    uint32_t yl =(uint32_t)(y >> 32), yr = (uint32_t)y;
    uint32_t kll =(uint32_t)(kl >> 32), klr = (uint32_t)kl;
    uint32_t xl = (yr | klr) ^ yl;
    uint32_t t = xl & kll;
    uint32_t xr = ((t << 1) | (t >> 31)) ^ yr;
    return ((uint64_t)xl << 32) | xr;
}

