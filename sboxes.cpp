uint64_t FL(uint64_t x, uint64_t kl) {
    uint32_t xl = (uint32_t)(x >> 32), xr = (uint32_t)x;
    uint32_t kll = (uint32_t)(kl >> 32), klr = (uint32_t)kl;
    uint32_t t = xl & kll;
    uint32_t yr = ((t << 1) | (t >> 31)) ^ xr;
    uint32_t yl = (yr|klr) ^ xl;
    return ((uint64_t)yl << 32) | yr;
}
